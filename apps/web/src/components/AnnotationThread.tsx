/**
 * @file AnnotationThread.tsx
 *
 * Annotation thread UI components for the wiki inline annotation system.
 *
 * ## Component hierarchy
 *
 *   AnnotationSidebar
 *   └── AnnotationThreadCard      — renders one thread with replies
 *         └── AnnotationReplyForm — inline reply input
 *
 *   AgentAnnotationThread         — renders a wiki_annotation entity thread
 *         └── AgentMessageBadge  — "Agent" label on every agent-authored message
 *
 * ## Usage
 *
 * Mount `AnnotationSidebar` alongside `WikiRender`. Pass the customerId and
 * versionId so it can fetch and post threads.
 *
 * Mount `AgentAnnotationThread` where annotation correction threads
 * (from the Anthropic API annotation flow) are shown. Each agent message will
 * display an "Agent" badge so users can never be confused about authorship.
 *
 * @see https://github.com/superfield-ai/superfield-kb-demo/issues/63
 * @see https://github.com/superfield-ai/superfield-kb-demo/issues/68
 */

import React, { useCallback, useEffect, useRef, useState } from 'react';

