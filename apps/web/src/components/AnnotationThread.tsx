/**
 * Renders a single annotation thread with its replies and a reply input.
 */
export function AnnotationThreadCard({
  thread,
  customerId,
  versionId,
  active,
  onClick,
  onResolved,
  onReplyPosted,
}: {
  thread: AnnotationThread;
  customerId: string;
  versionId: string;
  active: boolean;
  onClick: () => void;
  onResolved: (threadId: string, resolved: boolean) => void;
  onReplyPosted: (threadId: string, reply: AnnotationReply) => void;
}): React.ReactElement {
  const [replyBody, setReplyBody] = useState('');
  const [posting, setPosting] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const replyInputRef = useRef<HTMLTextAreaElement>(null);

  const handleReply = useCallback(
    async (e: React.FormEvent) => {
      e.preventDefault();
      if (!replyBody.trim()) return;
      setPosting(true);
      setError(null);
      try {
        const res = await fetch(
          `/api/wiki/pages/${encodeURIComponent(customerId)}/versions/${encodeURIComponent(versionId)}/annotations/${encodeURIComponent(thread.id)}/replies`,
          {
            method: 'POST',
            credentials: 'include',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ body: replyBody.trim() }),
          },
        );
        if (!res.ok) {
          const body = await res.json().catch(() => ({}));
          throw new Error((body as { error?: string }).error ?? `HTTP ${res.status}`);
        }
        const reply = (await res.json()) as AnnotationReply;
        onReplyPosted(thread.id, reply);
        setReplyBody('');
      } catch (err) {
        setError(err instanceof Error ? err.message : 'Failed to post reply');
      } finally {
        setPosting(false);
      }
    },
    [replyBody, customerId, versionId, thread.id, onReplyPosted],
  );

  const handleResolve = useCallback(async () => {
    const newResolved = !thread.resolved;
    try {
      const res = await fetch(
        `/api/wiki/pages/${encodeURIComponent(customerId)}/versions/${encodeURIComponent(versionId)}/annotations/${encodeURIComponent(thread.id)}`,
        {
          method: 'PATCH',
          credentials: 'include',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ resolved: newResolved }),
        },
      );
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      onResolved(thread.id, newResolved);
    } catch (err) {
      console.error('[AnnotationThreadCard] resolve failed:', err);
    }
  }, [customerId, versionId, thread.id, thread.resolved, onResolved]);

  return (
    <div
      data-testid="annotation-thread-card"
      data-thread-id={thread.id}
      onClick={onClick}
      className={`rounded-lg border transition-all cursor-pointer ${
        active
          ? 'border-indigo-400 bg-indigo-50'
          : thread.resolved
            ? 'border-zinc-200 bg-zinc-50 opacity-60'
            : 'border-zinc-200 bg-white hover:border-zinc-300'
      }`}
    >
      {/* Thread header */}
      <div className="px-3 pt-2.5 pb-1">
        <div className="flex items-start justify-between gap-2">
          <blockquote
            data-testid="annotation-anchor-text"
            className="text-[11px] text-zinc-500 italic border-l-2 border-indigo-300 pl-2 truncate max-w-[160px]"
          >
            {thread.anchor_text}
          </blockquote>
          {thread.resolved && (
            <span className="shrink-0 text-[10px] font-semibold uppercase tracking-wide text-emerald-600 bg-emerald-50 border border-emerald-200 px-1.5 py-0.5 rounded">
              resolved
            </span>
          )}
        </div>
        <p className="mt-1.5 text-xs text-zinc-800">{thread.body}</p>
        <p className="mt-1 text-[11px] text-zinc-400">{thread.created_by}</p>
      </div>

      {/* Replies */}
      {thread.replies.length > 0 && (
        <ul className="border-t border-zinc-100 mt-1">
          {thread.replies.map((reply) => (
            <li
              key={reply.id}
              data-testid="annotation-reply"
              className="px-3 py-2 border-b border-zinc-100 last:border-b-0"
            >
              <p className="text-xs text-zinc-800">{reply.body}</p>
              <p className="mt-0.5 text-[11px] text-zinc-400">{reply.created_by}</p>
            </li>
          ))}
        </ul>
      )}

      {/* Reply form — only visible when the thread is active and not resolved */}
      {active && !thread.resolved && (
        <form
          onSubmit={handleReply}
          onClick={(e) => e.stopPropagation()}
          className="border-t border-zinc-100 px-3 py-2"
        >
          <textarea
            ref={replyInputRef}
            data-testid="annotation-reply-input"
            value={replyBody}
            onChange={(e) => setReplyBody(e.target.value)}
            placeholder="Reply…"
            rows={2}
            className="w-full text-xs border border-zinc-200 rounded px-2 py-1 resize-none focus:outline-none focus:ring-2 focus:ring-indigo-400"
          />
          {error && <p className="mt-1 text-[11px] text-red-600">{error}</p>}
          <div className="mt-1.5 flex items-center justify-between gap-2">
            <button
              type="submit"
              disabled={posting || !replyBody.trim()}
              data-testid="annotation-reply-submit"
              className="text-[11px] font-medium text-white bg-indigo-500 hover:bg-indigo-600 disabled:opacity-50 px-2.5 py-1 rounded"
            >
              {posting ? 'Posting…' : 'Reply'}
            </button>
            <button
              type="button"
              onClick={handleResolve}
              data-testid="annotation-resolve-button"
              className="text-[11px] text-zinc-500 hover:text-zinc-700"
            >
              Resolve
            </button>
          </div>
        </form>
      )}

      {/* Re-open button when resolved and active */}
      {active && thread.resolved && (
        <div className="border-t border-zinc-100 px-3 py-2" onClick={(e) => e.stopPropagation()}>
          <button
            type="button"
            onClick={handleResolve}
            data-testid="annotation-reopen-button"
            className="text-[11px] text-zinc-500 hover:text-zinc-700"
          >
            Re-open
          </button>
        </div>
      )}
    </div>
  );
}

// ---------------------------------------------------------------------------
// NewThreadForm — popover for creating a thread from a text selection
// ---------------------------------------------------------------------------

export interface NewThreadAnchor {
  /** The selected text. */
  text: string;
  /** Character offset of selection start in the version content. */
  startOffset: number;
  /** Character offset of selection end in the version content. */
  endOffset: number;
  /** Client rect of the selection — for popover positioning. */
  rect: DOMRect;
}

export function NewThreadForm({
  anchor,
  customerId,
  versionId,
  onCreated,
  onCancel,
}: {
  anchor: NewThreadAnchor;
  customerId: string;
  versionId: string;
  onCreated: (thread: AnnotationThread) => void;
  onCancel: () => void;
}): React.ReactElement {
  const [body, setBody] = useState('');
  const [posting, setPosting] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const inputRef = useRef<HTMLTextAreaElement>(null);

  // Auto-focus when the form mounts.
  useEffect(() => {
    inputRef.current?.focus();
  }, []);

  const handleSubmit = useCallback(
    async (e: React.FormEvent) => {
      e.preventDefault();
      if (!body.trim()) return;
      setPosting(true);
      setError(null);
      try {
        const res = await fetch(
          `/api/wiki/pages/${encodeURIComponent(customerId)}/versions/${encodeURIComponent(versionId)}/annotations`,
          {
            method: 'POST',
            credentials: 'include',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({
              anchor_text: anchor.text,
              start_offset: anchor.startOffset,
              end_offset: anchor.endOffset,
              body: body.trim(),
            }),
          },
        );
        if (!res.ok) {
          const respBody = await res.json().catch(() => ({}));
          throw new Error((respBody as { error?: string }).error ?? `HTTP ${res.status}`);
        }
        const thread = (await res.json()) as AnnotationThread;
        onCreated(thread);
      } catch (err) {
        setError(err instanceof Error ? err.message : 'Failed to create thread');
      } finally {
        setPosting(false);
      }
    },
    [body, anchor, customerId, versionId, onCreated],
  );

  return (
    <form data-testid="new-thread-form" onSubmit={handleSubmit} className="flex flex-col gap-2">
      <div className="text-[11px] text-zinc-500 italic border-l-2 border-indigo-300 pl-2 truncate">
        {anchor.text}
      </div>
      <textarea
        ref={inputRef}
        data-testid="new-thread-body-input"
        value={body}
        onChange={(e) => setBody(e.target.value)}
        placeholder="Add a comment…"
        rows={3}
        className="text-xs border border-zinc-200 rounded px-2 py-1 resize-none focus:outline-none focus:ring-2 focus:ring-indigo-400"
      />
      {error && <p className="text-[11px] text-red-600">{error}</p>}
      <div className="flex items-center gap-2">
        <button
          type="submit"
          disabled={posting || !body.trim()}
          data-testid="new-thread-submit"
          className="text-[11px] font-medium text-white bg-indigo-500 hover:bg-indigo-600 disabled:opacity-50 px-2.5 py-1 rounded"
        >
          {posting ? 'Saving…' : 'Comment'}
        </button>
        <button
          type="button"
          onClick={onCancel}
          data-testid="new-thread-cancel"
          className="text-[11px] text-zinc-500 hover:text-zinc-700"
        >
          Cancel
        </button>
      </div>
    </form>
  );
}

