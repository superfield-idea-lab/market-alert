export interface AnnotationSidebarProps {
  customerId: string;
  versionId: string;
  /** Content of the current wiki version — used for fuzzy re-anchoring. */
  content: string;
  /** When set, a new-thread form is shown for this selection. */
  pendingAnchor: NewThreadAnchor | null;
  onPendingAnchorCleared: () => void;
  /** Notify parent when threads update so highlight markers can refresh. */
  onThreadsChange?: (threads: AnnotationThread[]) => void;
}

/**
 * AnnotationSidebar — lists threads for the current wiki version and handles
 * new-thread creation via a popover form.
 *
 * No mocks — fetches real data from the annotation threads API.
 */
export function AnnotationSidebar({
  customerId,
  versionId,
  content,
  pendingAnchor,
  onPendingAnchorCleared,
  onThreadsChange,
}: AnnotationSidebarProps): React.ReactElement {
  const [threads, setThreads] = useState<AnnotationThread[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);
  const [activeThreadId, setActiveThreadId] = useState<string | null>(null);

  // Fetch threads when versionId changes.
  useEffect(() => {
    let cancelled = false;
    setLoading(true);
    setError(null);

    fetch(
      `/api/wiki/pages/${encodeURIComponent(customerId)}/versions/${encodeURIComponent(versionId)}/annotations`,
      { credentials: 'include' },
    )
      .then(async (res) => {
        if (!res.ok) {
          const body = await res.json().catch(() => ({}));
          throw new Error((body as { error?: string }).error ?? `HTTP ${res.status}`);
        }
        return res.json() as Promise<{ threads: AnnotationThread[] }>;
      })
      .then((data) => {
        if (cancelled) return;
        setThreads(data.threads);
        onThreadsChange?.(data.threads);
      })
      .catch((err: unknown) => {
        if (cancelled) return;
        setError(err instanceof Error ? err.message : 'Unknown error');
      })
      .finally(() => {
        if (!cancelled) setLoading(false);
      });

    return () => {
      cancelled = true;
    };
  }, [customerId, versionId, onThreadsChange]);

  const handleThreadCreated = useCallback(
    (thread: AnnotationThread) => {
      const updated = [...threads, thread];
      setThreads(updated);
      onThreadsChange?.(updated);
      setActiveThreadId(thread.id);
      onPendingAnchorCleared();
    },
    [threads, onThreadsChange, onPendingAnchorCleared],
  );

  const handleResolved = useCallback(
    (threadId: string, resolved: boolean) => {
      const updated = threads.map((t) => (t.id === threadId ? { ...t, resolved } : t));
      setThreads(updated);
      onThreadsChange?.(updated);
    },
    [threads, onThreadsChange],
  );

  const handleReplyPosted = useCallback(
    (threadId: string, reply: AnnotationReply) => {
      const updated = threads.map((t) =>
        t.id === threadId ? { ...t, replies: [...t.replies, reply] } : t,
      );
      setThreads(updated);
      onThreadsChange?.(updated);
    },
    [threads, onThreadsChange],
  );

  // Compute re-anchored offsets for each thread so stale threads show a warning.
  const anchors = threads.map((t) => resolveAnchor(content, t));

  if (loading) {
    return (
      <div
        data-testid="annotation-sidebar"
        className="flex items-center justify-center h-16 text-zinc-400 text-xs"
      >
        Loading annotations…
      </div>
    );
  }

  if (error) {
    return (
      <div data-testid="annotation-sidebar" className="p-3 text-xs text-red-600">
        Failed to load annotations: {error}
      </div>
    );
  }

  const openThreads = threads.filter((t) => !t.resolved);
  const resolvedThreads = threads.filter((t) => t.resolved);

  return (
    <div data-testid="annotation-sidebar" className="flex flex-col gap-2 p-2">
      {/* New thread form */}
      {pendingAnchor && (
        <div className="rounded-lg border border-indigo-300 bg-indigo-50 p-3">
          <p className="text-[10px] font-semibold text-indigo-700 uppercase tracking-wide mb-2">
            New comment
          </p>
          <NewThreadForm
            anchor={pendingAnchor}
            customerId={customerId}
            versionId={versionId}
            onCreated={handleThreadCreated}
            onCancel={onPendingAnchorCleared}
          />
        </div>
      )}

      {/* Open threads */}
      {openThreads.length === 0 && !pendingAnchor && (
        <p className="text-xs text-zinc-400 text-center py-4">
          No annotations yet. Select text to add a comment.
        </p>
      )}

      {openThreads.map((thread) => (
        <div key={thread.id}>
          {anchors[threads.indexOf(thread)] === null && (
            <p className="text-[11px] text-amber-600 px-1 pb-1">
              Anchor unavailable — content changed.
            </p>
          )}
          <AnnotationThreadCard
            thread={thread}
            customerId={customerId}
            versionId={versionId}
            active={thread.id === activeThreadId}
            onClick={() => setActiveThreadId(thread.id === activeThreadId ? null : thread.id)}
            onResolved={handleResolved}
            onReplyPosted={handleReplyPosted}
          />
        </div>
      ))}

      {/* Resolved threads (collapsed section) */}
      {resolvedThreads.length > 0 && (
        <details className="mt-2">
          <summary className="text-[11px] text-zinc-400 cursor-pointer select-none">
            {resolvedThreads.length} resolved
          </summary>
          <div className="flex flex-col gap-2 mt-2">
            {resolvedThreads.map((thread) => (
              <AnnotationThreadCard
                key={thread.id}
                thread={thread}
                customerId={customerId}
                versionId={versionId}
                active={thread.id === activeThreadId}
                onClick={() => setActiveThreadId(thread.id === activeThreadId ? null : thread.id)}
                onResolved={handleResolved}
                onReplyPosted={handleReplyPosted}
              />
            ))}
          </div>
        </details>
      )}
    </div>
  );
}
