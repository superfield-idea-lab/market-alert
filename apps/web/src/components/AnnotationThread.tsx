/**
 * Author kind for an annotation message.
 * 'human' — written by the Records Manager.
 * 'agent' — generated by the Anthropic API annotation agent.
 */
export type AuthorKind = 'human' | 'agent';

/**
 * A single message in a wiki_annotation entity thread.
 * Mirrors apps/server/src/api/annotations.ts AnnotationMessage.
 */
export interface AgentAnnotationMessage {
  role: 'rm' | 'agent';
  author_kind: AuthorKind;
  content: string;
  created_at: string;
}

// ---------------------------------------------------------------------------
// AgentMessageBadge — visible "Agent" label on every agent-authored message
// ---------------------------------------------------------------------------

/**
 * Renders a small pill badge that labels a message as agent-authored.
 *
 * Always rendered for messages with author_kind='agent'. The badge text
 * "Agent" is visible at all times so users can never be confused about
 * whether they are talking to a human or an agent.
 */
export function AgentMessageBadge(): React.ReactElement {
  return (
    <span
      data-testid="agent-message-badge"
      className="inline-flex items-center gap-1 text-[10px] font-semibold uppercase tracking-wide text-violet-700 bg-violet-50 border border-violet-200 px-1.5 py-0.5 rounded"
    >
      Agent
    </span>
  );
}

// ---------------------------------------------------------------------------
// AgentAnnotationThread — renders a wiki_annotation entity thread with labels
// ---------------------------------------------------------------------------

/**
 * Renders the full message thread of a wiki_annotation entity.
 *
 * Every message with `author_kind='agent'` displays an `AgentMessageBadge`
 * so the user always knows which messages were generated by an AI agent.
 *
 * This component is the canonical UI surface for the agent visibility
 * requirement described in issue #68.
 */
export function AgentAnnotationThread({
  messages,
}: {
  messages: AgentAnnotationMessage[];
}): React.ReactElement {
  return (
    <ul data-testid="agent-annotation-thread" className="flex flex-col gap-2">
      {messages.map((msg, idx) => (
        <li
          key={idx}
          data-testid={
            msg.author_kind === 'agent' ? 'agent-annotation-message' : 'human-annotation-message'
          }
          className={`rounded-md border px-3 py-2 ${
            msg.author_kind === 'agent'
              ? 'border-violet-200 bg-violet-50'
              : 'border-zinc-200 bg-white'
          }`}
        >
          <div className="flex items-center gap-2 mb-1">
            <span className="text-[11px] font-medium text-zinc-500">
              {msg.author_kind === 'agent' ? 'Agent' : 'You'}
            </span>
            {msg.author_kind === 'agent' && <AgentMessageBadge />}
          </div>
          <p className="text-xs text-zinc-800">{msg.content}</p>
          <p className="mt-0.5 text-[10px] text-zinc-400">{msg.created_at}</p>
        </li>
      ))}
    </ul>
  );
}

// ---------------------------------------------------------------------------
// Types (mirror the server-side shapes)
// ---------------------------------------------------------------------------

export interface AnnotationReply {
  id: string;
  thread_id: string;
  body: string;
  created_by: string;
  created_at: string;
}

export interface AnnotationThread {
  id: string;
  wiki_version_id: string;
  anchor_text: string;
  start_offset: number;
  end_offset: number;
  body: string;
  created_by: string;
  resolved: boolean;
  resolved_by: string | null;
  resolved_at: string | null;
  created_at: string;
  updated_at: string;
  replies: AnnotationReply[];
}

// ---------------------------------------------------------------------------
// Anchor re-computation — fuzzy re-anchoring after small edits
// ---------------------------------------------------------------------------

/**
 * Re-anchors a thread against the current version content.
 *
 * Strategy (three-tier):
 *   1. Exact match at the stored offset — content unchanged.
 *   2. Substring search — finds the anchor text anywhere in the content.
 *   3. Unavailable — content changed too much; returns null.
 *
 * Returns the resolved (start, end) offsets or null when unavailable.
 */
export function resolveAnchor(
  content: string,
  thread: Pick<AnnotationThread, 'anchor_text' | 'start_offset' | 'end_offset'>,
): { start: number; end: number } | null {
  const { anchor_text, start_offset, end_offset } = thread;
  const expected = end_offset - start_offset;

  // 1. Exact offset match.
  if (content.slice(start_offset, end_offset) === anchor_text) {
    return { start: start_offset, end: end_offset };
  }

  // 2. Search for the anchor text anywhere in the content (smallest offset wins).
  const found = content.indexOf(anchor_text);
  if (found !== -1) {
    return { start: found, end: found + expected };
  }

  // 3. Unavailable.
  return null;
}

